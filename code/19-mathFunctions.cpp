#include <iostream>
#include <cmath>

using namespace std;

int main(){
   double x=2;
   cout << "Sine of " << x << " is " << sin(x) << endl;
   cout << "Cosine of " << x << " is " << cos(x) << endl;
   cout << "Tangent of " << x << " is " << tan(x) << endl;
   cout << "Arc Sine of " << x << " is " << asin(x) << endl;
   cout << "Arc Cosine of " << x << " is " << acos(x) << endl;
   cout << "Arc Tangent of " << x << " is " << atan(x) << endl;
   cout << "Hyperbolic sine of " << x << " is " << sinh(x) << endl;
   cout << "Hyperbolic cosine of " << x << " is " << cosh(x) << endl;
   cout << "Hyperbolic tangent of " << x << " is " << tanh(x) << endl;
   cout << "Exponential of " << x << " is " << exp(x) << endl;
   cout << "Logarithm (base 10) of " << x << " is " << log10(x) << endl;
   cout << "Natural logarithm of " << x << " is " << log(x) << endl;
   cout << "x^(3.5) of " << x << " is " << pow(x,3.5) << endl;
   cout << "Square root of " << x << " is " << sqrt(x) << endl;
   cout << "etc......." << endl;
   return 0;
}
