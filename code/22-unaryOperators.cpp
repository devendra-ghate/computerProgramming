#include <iostream>

using namespace std;

int main(){
   int i=1;
   double d=1.0;
   i++; d++;
   cout << "Integer increment " << i << endl;
   cout << "Double increment " << d << endl;
   i--; d--;
   cout << "Integer decrement " << i << endl;
   cout << "Double decrement " << d << endl;

   return 0;
}
